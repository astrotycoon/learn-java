enum Season {
	Spring, Summer, Autumn, Winter
}

public class Enum {
	public static void main(String args[]) {
		Season x = Season.Spring;
		System.out.println(x);
		
		x = Season.Summer;
		System.out.println(x);

		x = Season.Autumn;
		System.out.println(x);

		x = Season.Winter;
		System.out.println(x);		
	}
}
